namespace TPW
{
    public class Calculator
    {
        static void Main(string[] args)
        {
            Console.WriteLine("Hello");
        }

        public double add(double a, double b)
        {
            return a + b;
        }

        public double subtract(double a, double b)
        {
            return a - b;
        }

        public double multiply(double a, double b)
        {
            return a * b;
        }

